-- airplane.sql: Drop dependencies, create airplane table, and insert all records

-- 1. Disable FK checks and drop child then parent tables
SET FOREIGN_KEY_CHECKS = 0;
DROP TABLE IF EXISTS flight;
DROP TABLE IF EXISTS airplane;
SET FOREIGN_KEY_CHECKS = 1;

-- 2. Create the airplane table with AUTO_INCREMENT ID
CREATE TABLE airplane (
  ID INT NOT NULL AUTO_INCREMENT,
  airline_name VARCHAR(100) NOT NULL,
  seat_number INT NOT NULL,
  manufacturing_company VARCHAR(100),
  manufacturing_date DATE,
  age INT,
  PRIMARY KEY (ID),
  KEY idx_airline_plane (airline_name, ID),
  CONSTRAINT fk_airline
    FOREIGN KEY (airline_name)
    REFERENCES airline(name)
    ON UPDATE CASCADE
    ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

